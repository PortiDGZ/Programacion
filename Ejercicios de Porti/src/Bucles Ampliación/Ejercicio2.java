package Ejercicio5;

public class Ejercicio2 {

	public static void main(String[] args) {
		
		for(int i = 1000; i >= 5; i--) {
			
			System.out.println(i);
			
			
		}
		
		int i = 1000;
		do {
			
			System.out.println(i);
			
			i--;
			
		}while(i >= 5);

		i = 1000;
		
		while(i >= 5 & i <= 1000) {
			
			System.out.println(i);
			
			i--;
			
			
		}
	}
	
}

//El bucle for es el mas indicado ya que hay un número determinado de iteraciones
